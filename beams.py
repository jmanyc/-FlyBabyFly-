class Beams():

	def __init__(self, position, color):
		self.position = position
		self.color = color
		
	def setPosition(self, position):
		self.position = position
		
	def getPosition(self):
		return self.position
		
	def setColor(self, color):
		self.color = color
		
	def getColor(self):
		return self.color